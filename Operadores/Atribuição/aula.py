#Soma o valor à variável e atualiza a variável.
x = 5
x += 3  # x agora é 8
print(x)

#Subtrai o valor da variável e atualiza a variável.
x = 5
x -= 2  # x agora é 3
print(x)

#Multiplica o valor pela variável e atualiza a variável.
x = 5
x *= 2  # x agora é 10
print(x)

#Divide o valor pela variável e atualiza a variável.
x = 10
x /= 2  # x agora é 5.0
print(x)

#Realiza a divisão inteira e atualiza a variável.
x = 10
x //= 3  # x agora é 3
print(x)

#Calcula o resto da divisão e atualiza a variável.
x = 10
x %= 3  # x agora é 1
print(x)